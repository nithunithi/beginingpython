#range 

for number in range(9):
    print(number)

#we can use else in for 
for number in range(9):
    print(number)
else:
    print ('all numbers are finished')    

#nesting of loop
for number in range(9):
    print(number)
    for a in range (5):
        print(a)
else:
    print ('all numbers are finished')    

    